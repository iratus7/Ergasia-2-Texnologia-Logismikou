public class TimeStamp {
	
	int hour;
	int minute;
	int seconds;
	
	public TimeStamp(int h, int m, int s)
	{
		hour = h;
		minute = m;
		seconds = s;
	}
	
	public String toString()
	{
		return hour + ":" + minute + ":" + seconds;
	}
	
	
	
	
	
	
	

}




